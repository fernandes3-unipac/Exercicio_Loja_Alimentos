     public void getQuantidade2(String opcao){
            int quantidadeCategoriaAlimentos = 0;
            int quantidadeCategoriaEletronico = 0;
            int quantidadeCategoriaBebidas = 0;
            int quantidadeCategoriaVestuario = 0;
            int quantidadeCategoriaHigiene = 0;
            int quantidadeTotal = 0;
            jTextArea1.setText("");
            if (opcao.equals("CATEGORIA")){
            for(Venda v :  Banco.vendas){
                for(Produtos p : v.getProduto()){
                    if(p instanceof Alimentos){
                        quantidadeCategoriaAlimentos += p.getQuantidade();
                    }else if(p instanceof Eletronico){
                        quantidadeCategoriaEletronico += p.getQuantidade();
                    }else if(p instanceof Bebidas){
                        quantidadeCategoriaBebidas += p.getQuantidade();
                    }else if(p instanceof Vestuario){
                        quantidadeCategoriaVestuario += p.getQuantidade();
                    }else if(p instanceof Higiene){
                        quantidadeCategoriaHigiene += p.getQuantidade();
                    }
                        
                }
            }
            
            jTextArea1.setText("Alimentos : " + quantidadeCategoriaAlimentos + " unidades\n"
                              +"Eletrônicos : " + quantidadeCategoriaEletronico + " unidades\n"
                              +"Bebidas : " +  quantidadeCategoriaBebidas + " unidades\n" 
                              +"Vestuario : " + quantidadeCategoriaVestuario + " unidades\n"
                              +"Higiene : " +   quantidadeCategoriaHigiene + " unidades"
                            );
            }else if(opcao.equals("TODOS")){
                for(Venda v : Banco.vendas){
                    for(Produtos p : v.getProduto()){
                        quantidadeTotal += p.getQuantidade();
                    }
                }
                jTextArea1.setText("Total : " + quantidadeTotal + " unidades\n");
            }
            
        
    }
    
