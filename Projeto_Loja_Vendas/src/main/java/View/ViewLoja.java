    public ArrayList<Produtos> selectProdutos() {
        ArrayList<Produtos> produtosVenda = new ArrayList<>();
        String descricao;
        int quantidade;
        int IDProduto;

        for (int i = 0; i < vendaTable.getModel().getRowCount(); i++) {

            IDProduto = Integer.parseInt(vendaTable.getValueAt(i, 0).toString());
            descricao = vendaTable.getValueAt(i, 1).toString();
            quantidade = Integer.parseInt(vendaTable.getValueAt(i, 2).toString());
            System.out.println(IDProduto);
            System.out.println(quantidade);
            for (Produtos p : Banco.produtos) {
                if (p instanceof Alimentos && IDProduto == p.getIDProduto()) {
                    Alimentos alimento = new Alimentos(IDProduto, descricao, quantidade);
                    produtosVenda.add(alimento);
                    
                } else if (p instanceof Eletronico && IDProduto == p.getIDProduto()) {
                    Eletronico eletronico = new Eletronico(IDProduto, descricao, quantidade);
                    produtosVenda.add(eletronico);
                } else if (p instanceof Bebidas && IDProduto == p.getIDProduto()) {
                    Bebidas bebida = new Bebidas(IDProduto, descricao, quantidade);
                    produtosVenda.add(bebida);
                } else if (p instanceof Vestuario && IDProduto == p.getIDProduto()) {
                    Vestuario roupa = new Vestuario(IDProduto, descricao, quantidade);
                    produtosVenda.add(roupa);
                } else if (p instanceof Higiene && IDProduto == p.getIDProduto()) {
                    Higiene higiene = new Higiene(IDProduto, descricao, quantidade);
                    produtosVenda.add(higiene);
                }
            }

        }
        return produtosVenda;
    }

